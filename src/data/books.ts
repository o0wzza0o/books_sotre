export const getBookById = (id: number): Book | undefined =>
  books.find((b) => b.id === id);

export const getRelatedBooks = (book: Book, limit = 4): Book[] =>
  books.filter((b) => b.genre === book.genre && b.id !== book.id).slice(0, limit);

export const getFeaturedBooks = (): Book[] =>
  books.filter((b) => b.isBestseller).slice(0, 8);

export const getNewArrivals = (): Book[] =>
  books.filter((b) => b.isNew);

export const getTopRated = (): Book[] =>
  [...books].sort((a, b) => b.rating - a.rating).slice(0, 8);
